
// Adds the numbers
function add(input1, input2) {
    const total = (Number(input1) + Number(input2));
    return total;
}

// Subtracts numbers
function subtract(input1, input2) {
    const total = (Number(input1) - Number(input2));
    return total;
}

// Multiplies numbers
function multiply(input1, input2) {
    const total = (Number(input1) * Number(input2));
    return total;
}

// Divides numbers
function divide(input1, input2) {
    const total = (Number(input1) / Number(input2));
    return total;
}

alert(add(10, 10));

alert(subtract(10, 5));

alert(multiply(3, 7));

alert(divide(16, 4));
